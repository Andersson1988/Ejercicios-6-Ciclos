/* 1. Cree un programa que imprima los números enteros entre 0 y 100 en orden ascendente y descendente. 
      Use ciclo for. Use funciones. */

console.log(`------Numeros del 0 al 100 forma ascendente------`);
for (let numero=0;numero<=100;numero++) {
    console.log(`numero:${numero}`);
}

console.log(`------Numeros del 0 al 100 forma descendente------`);
for (let numero=100;numero>=0;numero--) {
    console.log(`numero:${numero}`);
}

